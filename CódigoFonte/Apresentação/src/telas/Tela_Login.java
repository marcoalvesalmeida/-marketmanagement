    protected void buscar() {
        Funcionario filtro;
        filtro = carregaFiltro();

        List<Funcionario> resultado = (List<Funcionario>) RepositorioBuilder.getFuncionarioRepositorio().buscar(filtro);

        preencherTabela(resultado);

    }

    protected void preencherTabela(List<Funcionario> dados) {
        for (Funcionario f : dados) {
            if (f.getEmail().equals(txtEmail.getText()) && f.getSenha().equals(txtSenha.getText())) {
                this.setVisible(false);
                MenuPrincipal nova = new MenuPrincipal();
                nova.setVisible(true);
                return;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Email ou senha incorretos!");
        txtEmail.setText("");
        txtSenha.setText("");
    }

    protected Funcionario carregaFiltro() {
        Funcionario entidade = new Funcionario();
        try {
            entidade.setEmail(txtEmail.getText());
            entidade.setSenha(txtSenha.getText());
            return entidade;
        } catch (ViolacaoRegraNegocioException ex) {
            Logger.getLogger(Tela_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    protected Funcionario novaEntidade() {
        return new Funcionario();
    }
