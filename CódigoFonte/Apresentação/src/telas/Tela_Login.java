    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (txtSenha.getText().length() < 8) {
            JOptionPane.showMessageDialog(rootPane, "A senha deve conter no mÃ­nimo 8 digitos!");
            return;
        }
        String email = txtEmail.getText();
        int pos = -1, count = 0;
        pos = email.indexOf('.');
        for (int i = 0; i < email.length(); i++) {
            if (email.charAt(i) == '@') {
                count++;
            }
        }
        if (email == null || pos == -1 || (count == 0 || count > 1)) {
            JOptionPane.showMessageDialog(rootPane, "O email deve conter apenas 1 arroba(@) e pelo menos 1 ponto!");
            return;
        }
        buscar();
    }//GEN-LAST:event_btnLoginActionPerformed

